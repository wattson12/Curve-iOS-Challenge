import UIKit

extension UIColor {

    static var background: UIColor {
        return UIColor(red: 0.255, green: 0.255, blue: 0.255, alpha: 1)
    }

    static var highRating: UIColor {
        return .green
    }

    static var mediumRating: UIColor {
        return .orange
    }

    static var lowRating: UIColor {
        return .red
    }
}
